package com.flashlighter.app;

public class FlashStates {
	
	private FlashStatesName name;
	private boolean state;

	/**
	 * @return the name
	 */
	public FlashStatesName getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(FlashStatesName name) {
		this.name = name;
	}

	/**
	 * @return the state
	 */
	public boolean isState() {
		return state;
	}

	/**
	 * @param state
	 *            the state to set
	 */
	public void setState(boolean state) {
		this.state = state;
	}

}
